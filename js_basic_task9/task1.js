// За допомогою setTimeout створіть функцію, яка приймає рядок, та кількість секунд.
// Функція повинна виводити переданий текст через передану кількість секунд.

console.log("Wake up")

setTimeout(function () {
    console.log("In the morning: breakfast")
}, 1000)

setTimeout(function () {
    console.log("In the afternoon: lunch")
}, 2000)

setTimeout(function () {
    console.log("In the evening: dinner")
}, 3000)

setTimeout(function () {
    console.log("Go to sleep")
}, 4000)


